# Logical Operators to chain multiple comparisions

f = 2 > 3
print(f)

g = 1 < 2 > 3
print(g)

h = 1 < 2 and 2 < 3
print(h)

i = 'h' == 'h' and 2 == 2 # and needs both of the conditions to be either true or false
print(i)

j = 'h' == 'i' or 2 == 2 # or needs either one of the conditions to be true.
print(j)


